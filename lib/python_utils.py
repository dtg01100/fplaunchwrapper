#!/usr/bin/env python3
"""Robust Python utilities for fplaunchwrapper
This script provides secure implementations of critical operations.
"""

from __future__ import annotations
import contextlib
import hashlib
import os
import re
import tempfile
import time
import unicodedata
from pathlib import Path
from typing import Any

from .paths import get_default_config_dir, get_lock_dir, ensure_dir

# Constants for file operations
# Can be overridden via environment variables for testing/customization
MAX_FILE_SIZE = int(os.environ.get("FPWRAPPER_MAX_FILE_SIZE", 100_000))  # 100KB limit
BUFFER_SIZE = int(os.environ.get("FPWRAPPER_BUFFER_SIZE", 8192))
DEFAULT_LOCK_TIMEOUT = float(os.environ.get("FPWRAPPER_LOCK_TIMEOUT", 30.0))

# Regex pattern for sanitizing strings - matches special characters that need escaping
_SANITIZE_PATTERN = re.compile(r'([\\"\'$`();&|<>\t\n\r])')


def sanitize_string(input_str: str) -> str:
    """Safely sanitize a string for use in Python code.

    Uses regex substitution for efficiency over sequential .replace() calls.
    """
    if not input_str:
        return ""
    return _SANITIZE_PATTERN.sub(r"\\\1", input_str)


def canonicalize_path_no_resolve(path: str | Path) -> Path | None:
    """Normalize a path without resolving symlinks."""
    try:
        path_str = str(path)

        if path_str.startswith("~"):
            path_str = str(Path(path_str).expanduser())

        if not Path(path_str).is_absolute():
            path_str = os.path.abspath(path_str)

        return Path(os.path.normpath(path_str))

    except (TypeError, ValueError, OSError, RuntimeError):
        return None


def validate_home_dir(dir_path: str | Path | None) -> str | None:
    """Validate that a directory is within HOME.

    Returns the normalized absolute path string if the directory is within the user's HOME,
    otherwise returns None. Accepts string or Path-like input.
    """
    try:
        if dir_path is None:
            return None
        dir_str = str(dir_path)

        if dir_str.startswith("~"):
            dir_str = str(Path(dir_str).expanduser())

        abs_dir = str(Path(dir_str).resolve())

        if Path(abs_dir).is_symlink():
            abs_dir = str(Path(abs_dir).resolve())

        home = str(Path.home())
        if abs_dir == home or abs_dir.startswith(home + os.sep):
            return str(abs_dir)

        return None
    except (TypeError, ValueError, OSError, RuntimeError):
        return None


def is_wrapper_file(file_path: str | Path) -> bool:
    """Check if a file is a valid wrapper script."""
    try:
        path = Path(file_path)
        if path.is_symlink():
            path = path.resolve()
        if not path.is_file():
            return False
        if not os.access(path, os.R_OK):
            return False

        size = path.stat().st_size
        if size > MAX_FILE_SIZE:
            return False

        with path.open(encoding="utf-8", errors="ignore") as f:
            content = f.read(min(BUFFER_SIZE, size))

        if any(ord(c) < 32 and c not in "\t\n\r" for c in content):
            return False

        if not re.match(r"^#!.*(bash|sh)", content, re.MULTILINE):
            return False

        if "Generated by fplaunchwrapper" not in content:
            return False

        name_match = re.search(r"^NAME=[^\n]*", content, re.MULTILINE)
        id_match = re.search(r"^ID=[^\n]*", content, re.MULTILINE)

        if not name_match or not id_match:
            return False

        id_value = re.search(r'ID="([^"]*)"', id_match.group())
        return not (not id_value or not re.match(r"^[A-Za-z0-9._-]+$", id_value.group(1)))
    except (IOError, OSError, UnicodeDecodeError, re.error):
        return False


def get_wrapper_id(file_path: str | Path) -> str | None:
    """Extract the wrapper ID from a wrapper script.

    Returns the ID string when found, otherwise returns None.
    """
    try:
        path = Path(file_path)
        if path.is_symlink():
            path = path.resolve()
        with path.open(encoding="utf-8", errors="ignore") as f:
            content = f.read(BUFFER_SIZE)

        id_match = re.search(r'^ID="([^"]*)"', content, re.MULTILINE)
        if id_match:
            return id_match.group(1)

        comment_match = re.search(r"Flatpak ID:\s*([^\s\n]+)", content)
        if comment_match:
            return comment_match.group(1)

        return None
    except (IOError, OSError, UnicodeDecodeError, re.error):
        return None


def sanitize_id_to_name(id_str: str) -> str:
    """Sanitize a Flatpak ID to a safe name."""
    try:
        name = id_str.rsplit(".", maxsplit=1)[-1].lower()

        try:
            name = unicodedata.normalize("NFKD", name)
            name = "".join(c for c in name if not unicodedata.combining(c))
        except ImportError:
            pass

        with contextlib.suppress(UnicodeError):
            name = name.encode("ascii", "ignore").decode("ascii")

        name = re.sub(r"[^a-z0-9_\-]", "-", name)

        name = re.sub(r"^\-+|\-+$", "", name)
        name = re.sub(r"\-+", "-", name)

        if not name:
            hash_obj = hashlib.sha256(id_str.encode("utf-8"))
            name = f"app-{hash_obj.hexdigest()[:8]}"

        return name[:100]

    except (TypeError, AttributeError, UnicodeDecodeError, re.error):
        try:
            return f"app-{hashlib.sha256(id_str.encode('utf-8')).hexdigest()[:8]}"
        except (TypeError, AttributeError, UnicodeDecodeError):
            return "app-fallback"


def find_executable(cmd: str) -> str | None:
    """Find an executable in PATH with security checks."""
    try:
        if "/" in cmd:
            if Path(cmd).is_file() and os.access(cmd, os.X_OK):
                # Resolve symlinks to prevent symlink attacks
                return str(Path(cmd).resolve())
            return None

        for path_dir in os.getenv("PATH", "").split(":"):
            if not path_dir:
                continue
            exe_path = str(Path(path_dir) / cmd)
            if Path(exe_path).is_file() and os.access(exe_path, os.X_OK):
                # Resolve symlinks to prevent symlink attacks
                return str(Path(exe_path).resolve())

        return None
    except (TypeError, OSError, AttributeError):
        return None


def safe_mktemp(
    template: str = "tmp.XXXXXX",
    dir_param: str | None = None,
) -> str | None:
    """Create a secure temporary file.

    Returns the created temporary file path, or None on failure.
    """
    try:
        if dir_param and Path(dir_param).is_dir():
            tdir = dir_param
        else:
            tdir = tempfile.gettempdir()

        if "XXXXXX" in template:
            parts = template.split("XXXXXX", 1)
            prefix = parts[0] if parts[0] else None
            suffix = parts[1] if len(parts) > 1 else ""
        else:
            prefix = template
            suffix = ""

        fd, path = tempfile.mkstemp(prefix=prefix, suffix=suffix, dir=tdir)
        os.close(fd)
        return path
    except (IOError, OSError, ValueError):
        return None


def acquire_lock(
    lock_name: str = "fplaunch",
    timeout_seconds: float = DEFAULT_LOCK_TIMEOUT,
    lock_dir: Path | None = None,
) -> bool | None:
    """Acquire a file-based lock with timeout.

    Uses mkdir() for atomic lock acquisition (O_EXCL flag ensures atomicity).
    Lock is stored as a file containing the PID of the holding process.

    Args:
        lock_name: Name of the lock
        timeout_seconds: How long to wait for lock acquisition
        lock_dir: Directory for lock files (defaults to config-based locks)
    """
    try:
        if lock_dir is None:
            config_dir = get_default_config_dir()
            ensure_dir(config_dir)
            lock_dir = get_lock_dir()
        ensure_dir(lock_dir)

        lockfile = lock_dir / f"{lock_name}.lock"
        pidfile = lock_dir / f"{lock_name}.pid"

        start_time = time.time()
        end_time = start_time + timeout_seconds

        while time.time() < end_time:
            # Clean up stale lock if it exists
            if lockfile.exists():
                if _cleanup_stale_lock(lockfile, pidfile):
                    continue  # Lock was cleaned, try to acquire again

            # Use mkdir for atomic lock acquisition
            # This is atomic even across network filesystems (NFS, etc.)
            try:
                lockfile.mkdir(parents=True, exist_ok=False)
                # Write PID file - use atomic write via tempfile + rename
                _write_pid_atomic(pidfile, os.getpid())
                return True
            except FileExistsError:
                # Lock acquired by another process, wait and retry
                time.sleep(0.1)
                continue
            except OSError:
                time.sleep(0.1)
                continue

        return False  # Timeout

    except (IOError, OSError, PermissionError, RuntimeError):
        return False


def _write_pid_atomic(pidfile: Path, pid: int) -> None:
    """Write PID file atomically using rename trick."""
    temp_pid = pidfile.parent / f"{pidfile.name}.tmp"
    timestamp = int(time.time())
    pid_data = f"{pid}:{timestamp}"
    try:
        temp_pid.write_text(pid_data)
        temp_pid.rename(pidfile)  # Atomic on POSIX
    except OSError:
        # Fallback: direct write if rename fails
        if temp_pid.exists():
            with contextlib.suppress(OSError):
                temp_pid.unlink()
        pidfile.write_text(pid_data)


def atomic_write_text(
    target: Path,
    content: str,
    *,
    mode: int | None = None,
    encoding: str = "utf-8",
) -> None:
    r"""Atomically write ``content`` to ``target``.

    Writes to a sibling temp file in the same directory, fsyncs, then
    ``os.replace``\ s onto ``target``. ``os.replace`` is atomic on POSIX
    and refuses to follow a symlink at the destination, defeating the
    TOCTOU window where an attacker plants a symlink between an existence
    check and a write.

    On failure, the temp file is cleaned up and the original ``target``
    is left untouched. ``mode`` (e.g. ``0o600``) is applied to the
    destination after the rename.
    """
    target = Path(target)
    target.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp_path_str = tempfile.mkstemp(
        prefix=f".{target.name}.",
        suffix=".tmp",
        dir=str(target.parent),
    )
    tmp_path = Path(tmp_path_str)
    try:
        with os.fdopen(fd, "w", encoding=encoding) as f:
            f.write(content)
            f.flush()
            os.fsync(f.fileno())
        os.replace(tmp_path, target)
    except BaseException:
        with contextlib.suppress(OSError):
            tmp_path.unlink()
        raise
    if mode is not None:
        with contextlib.suppress(OSError):
            target.chmod(mode)


def atomic_write_bytes(target: Path, content: bytes, *, mode: int | None = None) -> None:
    """Atomically write ``content`` to ``target``. See :func:`atomic_write_text`."""
    target = Path(target)
    target.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp_path_str = tempfile.mkstemp(
        prefix=f".{target.name}.",
        suffix=".tmp",
        dir=str(target.parent),
    )
    tmp_path = Path(tmp_path_str)
    try:
        with os.fdopen(fd, "wb") as f:
            f.write(content)
            f.flush()
            os.fsync(f.fileno())
        os.replace(tmp_path, target)
    except BaseException:
        with contextlib.suppress(OSError):
            tmp_path.unlink()
        raise
    if mode is not None:
        with contextlib.suppress(OSError):
            target.chmod(mode)



def _cleanup_stale_lock(lockfile: Path, pidfile: Path, stale_timeout: int = 60) -> bool:
    """Check if lock is stale and clean it up if the process is dead.

    Returns True if lock was cleaned up, False otherwise.

    Note: stale_timeout parameter kept for API compatibility but PID check
    is the primary staleness detection. A lock is stale if:
    1. PID file doesn't exist, OR
    2. PID is not alive (process not running), OR
    3. Lock timestamp exceeds stale_timeout (defensive check)
    """
    try:
        if not pidfile.exists():
            with contextlib.suppress(OSError):
                lockfile.rmdir()
            return True

        try:
            content = pidfile.read_text().strip()
            parts = content.split(":")
            stored_pid = int(parts[0])

            # Check if PID is alive - primary staleness check
            try:
                os.kill(stored_pid, 0)
                # PID is alive - check timestamp for defensive staleness
                if len(parts) >= 2:
                    try:
                        stored_timestamp = int(parts[1])
                        current_time = int(time.time())
                        if current_time - stored_timestamp > stale_timeout:
                            # Timestamp is stale even though PID appears alive
                            # This handles edge case of PID reuse after long delay
                            with contextlib.suppress(OSError):
                                lockfile.rmdir()
                                pidfile.unlink(missing_ok=True)
                            return True
                    except ValueError:
                        pass  # Invalid timestamp, rely on PID check
                return False  # PID is alive and timestamp is fresh
            except OSError:
                # PID is not alive - clean up the lock
                pass  # Fall through to cleanup
        except (ValueError, OSError):
            pass  # Can't parse PID, fall through to cleanup

        # Clean up the lock
        with contextlib.suppress(OSError):
            lockfile.rmdir()
            pidfile.unlink(missing_ok=True)
        return True

    except (OSError, ValueError, IOError):
        return False


def release_lock(lock_name: str = "fplaunch", lock_dir: Path | None = None) -> bool | None:
    """Release a file-based lock."""
    try:
        if lock_dir is None:
            lock_dir = get_lock_dir()
        lockfile = lock_dir / f"{lock_name}.lock"
        pidfile = lock_dir / f"{lock_name}.pid"

        if pidfile.exists():
            content = pidfile.read_text().strip()
            # Parse PID from new format (pid:timestamp) or old format (just pid)
            parts = content.split(":")
            stored_pid = parts[0]
            if stored_pid == str(os.getpid()):
                with contextlib.suppress(FileNotFoundError):
                    lockfile.rmdir()  # lockfile is a directory, not a file
                with contextlib.suppress(FileNotFoundError):
                    pidfile.unlink()
                return True

        return False

    except (IOError, OSError, PermissionError):
        return False


def get_temp_dir() -> str:
    """Get the best available temporary directory.

    Tries ``tempfile.gettempdir()`` first, then ``$HOME/.cache``. Raises
    :class:`OSError` when neither location is writable, so callers fail
    loudly with a clear cause instead of receiving a path they cannot use.
    """
    candidates = [tempfile.gettempdir(), str(Path.home() / ".cache")]
    for candidate in candidates:
        if candidate and Path(candidate).is_dir() and os.access(candidate, os.W_OK):
            return candidate
    raise OSError(
        f"No writable temporary directory: tried {', '.join(c for c in candidates if c)}",
    )

