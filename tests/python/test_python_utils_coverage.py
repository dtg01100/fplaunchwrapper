class TestIsWrapperFile:
    def test_non_wrapper_returns_false(self, tmp_path: Path) -> None:
        target = tmp_path / "regular.txt"
        target.write_text("hello world\n")
        target.chmod(0o644)
        assert is_wrapper_file(target) is False

    def test_valid_wrapper_returns_true(self, tmp_path: Path) -> None:
        target = _write_wrapper(tmp_path / "wrap.sh")
        assert is_wrapper_file(target) is True

    def test_no_shebang_returns_false(self, tmp_path: Path) -> None:
        target = tmp_path / "no-shebang.sh"
        target.write_text("# Generated by fplaunchwrapper\nNAME=\"x\"\nID=\"y\"\n")
        target.chmod(0o644)
        assert is_wrapper_file(target) is False

    def test_symlink_wrapper(self, tmp_path: Path) -> None:
        target = _write_wrapper(tmp_path / "wrap.sh")
        link = tmp_path / "link.sh"
        link.symlink_to(target)
        assert is_wrapper_file(link) is True

    def test_unreadable_file_returns_false(self, tmp_path: Path) -> None:
        target = tmp_path / "wrap.sh"
        target.write_text("#!/bin/bash\n# Generated by fplaunchwrapper\nNAME=\"x\"\nID=\"y\"\n")
        target.chmod(0o000)
        try:
            if os.geteuid() == 0:
                pytest.skip("root bypasses os.access R_OK checks")
            assert is_wrapper_file(target) is False
        finally:
            target.chmod(0o644)

    def test_control_char_returns_false(self, tmp_path: Path) -> None:
        target = _write_wrapper(
            tmp_path / "wrap.sh",
            "#!/bin/bash\n# Generated by fplaunchwrapper\x01\nNAME=\"x\"\nID=\"y\"\n",
        )
        assert is_wrapper_file(target) is False

    def test_missing_name_and_id_returns_false(self, tmp_path: Path) -> None:
        target = _write_wrapper(
            tmp_path / "wrap.sh",
            "#!/bin/bash\n# Generated by fplaunchwrapper\n",
        )
        assert is_wrapper_file(target) is False

    def test_invalid_id_chars_returns_false(self, tmp_path: Path) -> None:
        target = _write_wrapper(
            tmp_path / "wrap.sh",
            "#!/bin/bash\n# Generated by fplaunchwrapper\nNAME=\"x\"\nID=\"org mozilla firefox\"\n",
        )
        assert is_wrapper_file(target) is False


