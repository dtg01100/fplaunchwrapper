VALID_WRAPPER = (
    "#!/bin/bash\n"
    "# Generated by fplaunchwrapper\n"
    'NAME="firefox"\n'
    'ID="org.mozilla.firefox"\n'
)


def _write_wrapper(path: Path, content: str = VALID_WRAPPER, executable: bool = True) -> Path:
    path.write_text(content, encoding="utf-8")
    if executable:
        path.chmod(0o755)
    return path


