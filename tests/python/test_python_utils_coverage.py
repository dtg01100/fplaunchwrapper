# _cleanup_stale_lock
# ---------------------------------------------------------------------------
class TestCleanupStaleLock:
    def test_no_pidfile_removes_lock(self, tmp_path: Path) -> None:
        lockfile = tmp_path / "test.lock"
        pidfile = tmp_path / "test.pid"
        lockfile.mkdir()
        result = _cleanup_stale_lock(lockfile, pidfile)
        assert result is True
        assert not lockfile.exists()

    def test_fresh_lock_returns_false(self, tmp_path: Path) -> None:
        lockfile = tmp_path / "test.lock"
        pidfile = tmp_path / "test.pid"
        lockfile.mkdir()
        fresh_timestamp = int(time.time())
        pidfile.write_text(f"{os.getpid()}:{fresh_timestamp}")
        result = _cleanup_stale_lock(lockfile, pidfile)
        assert result is False
        assert lockfile.exists()
        assert pidfile.exists()

    def test_stale_timeout_exceeded(self, tmp_path: Path) -> None:
        lockfile = tmp_path / "test.lock"
        pidfile = tmp_path / "test.pid"
        lockfile.mkdir()
        old_timestamp = int(time.time()) - 120
        pidfile.write_text(f"{os.getpid()}:{old_timestamp}")
        result = _cleanup_stale_lock(lockfile, pidfile, stale_timeout=60)
        assert result is True
        assert not lockfile.exists()
        assert not pidfile.exists()

    def test_dead_pid_removes_lock(self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        lockfile = tmp_path / "test.lock"
        pidfile = tmp_path / "test.pid"
        lockfile.mkdir()
        pidfile.write_text("12345:0")

        def mock_kill(pid: int, sig: int) -> None:
            raise OSError(f"No such process: {pid}")

        monkeypatch.setattr("os.kill", mock_kill)
        result = _cleanup_stale_lock(lockfile, pidfile)
        assert result is True
        assert not lockfile.exists()
        assert not pidfile.exists()

    def test_invalid_pid_format_falls_through_to_cleanup(self, tmp_path: Path) -> None:
        lockfile = tmp_path / "test.lock"
        pidfile = tmp_path / "test.pid"
        lockfile.mkdir()
        pidfile.write_text("not-a-number:0")
        result = _cleanup_stale_lock(lockfile, pidfile)
        assert result is True
        assert not lockfile.exists()
