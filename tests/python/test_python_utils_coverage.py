class TestAcquireLock:
    def test_happy_path(self, tmp_path: Path) -> None:
        lock_dir = tmp_path / "locks"
        lock_dir.mkdir()
        lock_name = "happy-path-test"
        try:
            result = acquire_lock(lock_name, timeout_seconds=2, lock_dir=lock_dir)
            assert result is True
        finally:
            release_lock(lock_name, lock_dir=lock_dir)

    def test_default_lock_dir_uses_config_dir(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        monkeypatch.setenv("XDG_CONFIG_HOME", str(tmp_path))
        monkeypatch.setenv("HOME", str(tmp_path))
        lock_name = f"default-lock-{os.getpid()}"
        try:
            result = acquire_lock(lock_name, timeout_seconds=2)
            assert result is True
        finally:
            release_lock(lock_name)

    def test_cleans_up_stale_lock_and_acquires(self, tmp_path: Path) -> None:
        lock_dir = tmp_path / "locks"
        lock_dir.mkdir()
        lock_name = "stale-lock-test"
        lockfile = lock_dir / f"{lock_name}.lock"
        pidfile = lock_dir / f"{lock_name}.pid"

        lockfile.mkdir()

        try:
            result = acquire_lock(lock_name, timeout_seconds=2, lock_dir=lock_dir)
            assert result is True
            assert pidfile.exists()
        finally:
            release_lock(lock_name, lock_dir=lock_dir)

    def test_oserror_on_mkdir_times_out(self, tmp_path: Path) -> None:
        lock_dir = tmp_path / "locks"
        lock_dir.mkdir()
        lock_name = "oserror-mkdir-test"

        real_mkdir = Path.mkdir

        def mock_mkdir(self: Path, *args: Any, **kwargs: Any) -> None:
            if str(self).endswith(".lock"):
                raise OSError("simulated")
            return real_mkdir(self, *args, **kwargs)

        with mock.patch.object(Path, "mkdir", mock_mkdir):
            result = acquire_lock(lock_name, timeout_seconds=0.2, lock_dir=lock_dir)
        assert result is False

    def test_outer_exception_returns_false(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        mock_ensure = mock.MagicMock(side_effect=PermissionError("simulated"))
        monkeypatch.setattr("lib.python_utils.ensure_dir", mock_ensure)
        result = acquire_lock("outer-exc-test", timeout_seconds=0.5, lock_dir=tmp_path)
        assert result is False


