class TestSanitizeIdToName:
    def test_normal_id(self) -> None:
        assert sanitize_id_to_name("org.mozilla.firefox") == "firefox"

    def test_dots_in_id(self) -> None:
        assert sanitize_id_to_name("org.example.myapp") == "myapp"

    def test_slashes_in_id(self) -> None:
        result = sanitize_id_to_name("org/example/app")
        assert "/" not in result
        assert "app" in result

    def test_special_chars_in_id(self) -> None:
        result = sanitize_id_to_name("org.example.app!@#")
        assert "!" not in result
        assert "@" not in result
        assert "#" not in result
        assert "app" in result

    def test_empty_string_falls_back_to_hash(self) -> None:
        result = sanitize_id_to_name("")
        assert result.startswith("app-")

    def test_none_input_falls_back(self) -> None:
        result = sanitize_id_to_name(None)  # type: ignore[arg-type]
        assert result.startswith("app-")

    def test_unicodedata_importerror_branch(self) -> None:
        class FakeUnicode:
            @staticmethod
            def normalize(*_args: object, **_kwargs: object) -> str:
                raise ImportError("simulated")

            @staticmethod
            def combining(*_args: object, **_kwargs: object) -> int:
                return 0

        with mock.patch("lib.python_utils.unicodedata", FakeUnicode):
            result = sanitize_id_to_name("org.mozilla.firefox")
        assert result == "firefox"


