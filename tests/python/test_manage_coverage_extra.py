class TestSetPreferenceBranches:
    """Cover all set_preference branches (156, 220-221, 242, 255, 257-259)."""

    def test_set_preference_existing_wrapper_returns_true(
        self, temp_env: dict[str, Path]
    ) -> None:
        """Happy path: wrapper exists, preference is written to the .pref file."""
        _make_wrapper(temp_env["bin_dir"], "firefox")
        mgr = _make_manager(temp_env)
        assert mgr.set_preference("firefox", "flatpak") is True
