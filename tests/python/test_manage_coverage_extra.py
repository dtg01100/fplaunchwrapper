class TestRemoveWrapperBranches:
    """Cover remove_wrapper for missing wrappers, force=True, and user-cancelled."""

    def test_remove_wrapper_nonexistent_returns_false(
        self, temp_env: dict[str, Path]
    ) -> None:
        """Removing a missing wrapper returns False (lines 172-174)."""
        mgr = _make_manager(temp_env)
        assert mgr.remove_wrapper("ghost", force=True) is False

    def test_remove_wrapper_force_true_removes_file_and_prefs(
        self, temp_env: dict[str, Path]
    ) -> None:
        """force=True bypasses the prompt and cleans up wrapper + prefs."""
        _make_wrapper(temp_env["bin_dir"], "firefox")
        (temp_env["config_dir"] / "firefox.pref").write_text("flatpak")
        (temp_env["config_dir"] / "firefox.env").write_text("FOO=bar")
        mgr = _make_manager(temp_env)
        assert mgr.remove_wrapper("firefox", force=True) is True
        assert not (temp_env["bin_dir"] / "firefox").exists()
        assert not (temp_env["config_dir"] / "firefox.pref").exists()
        assert not (temp_env["config_dir"] / "firefox.env").exists()

    def test_remove_wrapper_user_cancels_returns_false(
        self, temp_env: dict[str, Path]
    ) -> None:
        """force=False with Confirm.ask returning False should not delete (line 180)."""
        _make_wrapper(temp_env["bin_dir"], "firefox")
        mgr = _make_manager(temp_env)
        with patch("rich.prompt.Confirm") as mock_confirm:
            mock_confirm.ask.return_value = False
            assert mgr.remove_wrapper("firefox") is False
        # The wrapper is still there.
        assert (temp_env["bin_dir"] / "firefox").exists()

    def test_remove_wrapper_bin_dir_none_returns_false(self) -> None:
        """remove_wrapper with bin_dir=None returns False (lines 162-164)."""
        mgr = WrapperManager(emit_mode=True)
        mgr.bin_dir = None
        assert mgr.remove_wrapper("firefox", force=True) is False

    def test_remove_wrapper_invalid_name_returns_false(
        self, temp_env: dict[str, Path]
    ) -> None:
        """Invalid wrapper names are rejected by validate_wrapper_name."""
        mgr = _make_manager(temp_env)
        assert mgr.remove_wrapper("../etc/passwd", force=True) is False
        assert mgr.remove_wrapper("-flag", force=True) is False

    def test_remove_wrapper_config_dir_none_raises(self) -> None:
        """config_dir=None with emit_mode=False raises RuntimeError (line 187)."""
        tmp = Path(tempfile.mkdtemp(prefix="fpwrapper_rm_cfg_"))
        try:
            bin_dir = tmp / "bin"
            config_dir = tmp / "config"
            bin_dir.mkdir()
            config_dir.mkdir()
            _make_wrapper(bin_dir, "firefox")
            mgr = WrapperManager(bin_dir=str(bin_dir), config_dir=str(config_dir))
            mgr.config_dir = None
            mgr.emit_mode = False
            with pytest.raises(RuntimeError, match="config_dir is required"):
                mgr.remove_wrapper("firefox", force=True)
        finally:
            shutil.rmtree(tmp, ignore_errors=True)

    def test_remove_wrapper_oserror_returns_false(
        self, temp_env: dict[str, Path]
    ) -> None:
        """An OSError while unlinking the wrapper returns False (lines 213-215)."""
        _make_wrapper(temp_env["bin_dir"], "firefox")
        mgr = _make_manager(temp_env)
        wrapper_path = temp_env["bin_dir"] / "firefox"

        real_unlink = Path.unlink

        def _raise(self: Path, *args: object, **kwargs: object) -> None:
            if self == wrapper_path:
                raise OSError("synthetic unlink failure")
            return real_unlink(self, *args, **kwargs)

        with patch.object(Path, "unlink", _raise):
            assert mgr.remove_wrapper("firefox", force=True) is False

    def test_remove_wrapper_cleans_scripts_dir(
        self, temp_env: dict[str, Path]
    ) -> None:
        """A scripts/<name>/ directory inside config_dir should be removed."""
        _make_wrapper(temp_env["bin_dir"], "firefox")
        scripts_dir = temp_env["config_dir"] / "scripts" / "firefox"
        scripts_dir.mkdir(parents=True)
        (scripts_dir / "pre.sh").write_text("#!/bin/sh\necho pre\n")
        mgr = _make_manager(temp_env)
        assert mgr.remove_wrapper("firefox", force=True) is True
        assert not scripts_dir.exists()

    def test_remove_wrapper_refuses_escape_scripts_dir(
        self, temp_env: dict[str, Path], monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """A scripts_dir that escapes config_dir is refused (lines 206-209)."""
        _make_wrapper(temp_env["bin_dir"], "firefox")

        # Build a scripts_dir under config_dir, then symlink the parent
        # scripts directory to point at a location outside config_dir.
        outside = temp_env["temp_dir"] / "outside"
        outside.mkdir()
        outside_target = outside / "firefox"
        outside_target.mkdir()
        (outside_target / "pre.sh").write_text("#!/bin/sh\n")

        # Create scripts/<name> as a symlink that resolves outside config_dir
        scripts_link = temp_env["config_dir"] / "scripts" / "firefox"
        scripts_link.parent.mkdir(parents=True, exist_ok=True)
        try:
            scripts_link.symlink_to(outside_target)
        except (OSError, NotImplementedError):
            pytest.skip("symlinks not supported on this filesystem")

        mgr = _make_manager(temp_env)
        assert mgr.remove_wrapper("firefox", force=True) is False
        # The wrapper itself should still be removed (unlink happened first)
        assert not (temp_env["bin_dir"] / "firefox").exists()


