#!/usr/bin/env python3
"""Additional edge-path coverage tests for lib/manage.WrapperManager.

Targets the gaps reported by the coverage tool (88% baseline) and the
explicit edge cases listed in the task:

  - list_wrappers with no wrappers found
  - show_info success / failure / emit-mode / verbose warnings
  - set_preference (existing, missing, invalid preference value, OSError)
  - set_preference_all (happy path, no wrappers, partial failure)
  - create_alias (happy path, duplicate, non-existent source, OSError)
  - remove_wrapper (missing, force=True, user-cancelled, OSError)
  - discover_features

Tests use isolated temp directories and unittest.mock.patch where the
filesystem or interactive prompt would otherwise leak into the host.
"""

from __future__ import annotations

import shutil
import tempfile
from pathlib import Path
from unittest.mock import patch

import pytest

from lib.manage import WrapperManager, main


