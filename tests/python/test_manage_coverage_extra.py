@pytest.fixture
def temp_env() -> dict[str, Path]:
    """Create an isolated bin_dir + config_dir pair for the test."""
    temp_dir = Path(tempfile.mkdtemp(prefix="fpwrapper_mgmt_extra_"))
    bin_dir = temp_dir / "bin"
    config_dir = temp_dir / "config"
    bin_dir.mkdir()
    config_dir.mkdir()
    yield {"temp_dir": temp_dir, "bin_dir": bin_dir, "config_dir": config_dir}
    shutil.rmtree(temp_dir, ignore_errors=True)


def _make_wrapper(bin_dir: Path, name: str, flatpak_id: str | None = None) -> Path:
    """Create a valid-looking wrapper script in bin_dir."""
    wrapper = bin_dir / name
    flatpak_id = flatpak_id or f"org.test.{name}"
    wrapper.write_text(
        "#!/usr/bin/env bash\n"
        "# Generated by fplaunchwrapper\n"
        f'NAME="{name}"\n'
        f'ID="{flatpak_id}"\n'
        f"echo {name}\n"
    )
    wrapper.chmod(0o755)
    return wrapper


def _make_manager(temp_env: dict[str, Path], **kwargs: object) -> WrapperManager:
    """Build a WrapperManager bound to the isolated temp directories."""
    return WrapperManager(
        bin_dir=str(temp_env["bin_dir"]),
        config_dir=str(temp_env["config_dir"]),
        **kwargs,
    )


