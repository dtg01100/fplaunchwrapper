class TestListWrappersEdgeCases:
    """Cover the lines 104-106 verbose-warning branch and the empty case."""

    def test_list_wrappers_empty_directory(self, temp_env: dict[str, Path]) -> None:
        """Empty bin_dir should produce an empty list."""
        mgr = _make_manager(temp_env)
        assert mgr.list_wrappers() == []

    def test_list_wrappers_skips_non_executable(self, temp_env: dict[str, Path]) -> None:
        """A wrapper-looking file that is not executable should be skipped."""
        wrapper = temp_env["bin_dir"] / "notexec"
        wrapper.write_text(
            "#!/usr/bin/env bash\n"
            "# Generated by fplaunchwrapper\n"
            'ID="org.test.notexec"\n'
        )
        # Explicitly NOT chmod'ing it
        mgr = _make_manager(temp_env)
        assert mgr.list_wrappers() == []

    def test_list_wrappers_verbose_warning_on_unreadable(
        self, temp_env: dict[str, Path], capsys: pytest.CaptureFixture[str]
    ) -> None:
        """OSError reading a non-wrapper, executable file should be logged in verbose mode.

        Covers lines 104-106 (the except OSError branch).
        """
        # A non-wrapper file that IS executable will fall into the
        # else branch and trigger item.read_text(); mock that to raise.
        legacy = temp_env["bin_dir"] / "broken"
        legacy.write_text("#!/bin/bash\necho broken\n")
        legacy.chmod(0o755)

        mgr = _make_manager(temp_env, verbose=True)

        real_read_text = Path.read_text

        def _raise(self: Path, *args: object, **kwargs: object) -> str:
            if self == legacy:
                raise OSError("synthetic read failure")
            return real_read_text(self, *args, **kwargs)

        with patch.object(Path, "read_text", _raise):
            wrappers = mgr.list_wrappers()

        # The broken file is skipped, list is empty.
        assert wrappers == []
        captured = capsys.readouterr()
        assert "broken" in captured.err
        assert "Could not read" in captured.err


