class TestSetPreferenceAllBranches:
    """Cover the happy path, no-wrappers case, and the failed>0 path (lines 282, 284)."""

    def test_set_preference_all_happy_path(
        self, temp_env: dict[str, Path]
    ) -> None:
        """All wrappers get the new preference and the count is returned."""
        for name in ("firefox", "chrome", "vlc"):
            _make_wrapper(temp_env["bin_dir"], name)
        mgr = _make_manager(temp_env)
        count = mgr.set_preference_all("auto")
        assert count == 3
        for name in ("firefox", "chrome", "vlc"):
            assert (
                temp_env["config_dir"] / f"{name}.pref"
            ).read_text().strip() == "auto"

    def test_set_preference_all_no_wrappers_returns_zero(
        self, temp_env: dict[str, Path]
    ) -> None:
        """With no wrappers, set_preference_all returns 0."""
        mgr = _make_manager(temp_env)
        assert mgr.set_preference_all("flatpak") == 0

    def test_set_preference_all_invalid_preference_returns_zero(
        self, temp_env: dict[str, Path]
    ) -> None:
        """An invalid preference short-circuits to 0 before any writes."""
        _make_wrapper(temp_env["bin_dir"], "firefox")
        mgr = _make_manager(temp_env)
        assert mgr.set_preference_all("bogus") == 0
        assert not (temp_env["config_dir"] / "firefox.pref").exists()

    def test_set_preference_all_partial_failure(
        self, temp_env: dict[str, Path]
    ) -> None:
        """When some wrappers can't be updated, the failed branch runs (lines 282, 284).

        We force one wrapper to be missing between list_wrappers() and the
        inner set_preference() by deleting it from disk after the list is
        built. The remaining wrapper still gets updated.
        """
        for name in ("firefox", "chrome"):
            _make_wrapper(temp_env["bin_dir"], name)
        mgr = _make_manager(temp_env)

        # Patch set_preference so that the second call returns False
        original_set_pref = mgr.set_preference
        calls = {"n": 0}

        def _patched(name: str, preference: str) -> bool:
            calls["n"] += 1
            if calls["n"] == 2:
                return False
            return original_set_pref(name, preference)

        with patch.object(mgr, "set_preference", _patched):
            count = mgr.set_preference_all("flatpak")

        assert count == 1


