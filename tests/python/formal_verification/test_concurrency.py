def _proc_generate(
    bin_dir_str: str, config_dir_str: str, app_id: str, out_q: Any
) -> None:
    """Worker run in a subprocess for ``test_cross_process_generate_no_partial``."""
    try:
        gen = WrapperGenerator(
            bin_dir=bin_dir_str, config_dir=config_dir_str
        )
        ok = gen.generate_wrapper(app_id)
        out_q.put(("ok", ok))
    except BaseException as e:  # noqa: BLE001
        out_q.put(("error", repr(e)))


def _proc_remove(
    bin_dir_str: str, config_dir_str: str, name: str, out_q: Any
) -> None:
    """Worker run in a subprocess for cross-process remove races."""
    try:
        mgr = WrapperManager(bin_dir=bin_dir_str, config_dir=config_dir_str)
        out_q.put(("ok", mgr.remove_wrapper(name, force=True)))
    except BaseException as e:  # noqa: BLE001
        out_q.put(("error", repr(e)))


class TestCrossProcess:
    """Two processes operating on the same ``bin_dir``/``config_dir`` must
    converge to a state where every wrapper on disk is a complete, valid
    wrapper script — no zero-byte files, no broken symlinks.
    """

    def test_cross_process_generate_no_partial_artifacts(
        self, tmp_path: Path
    ) -> None:
        bin_dir = tmp_path / "bin"
        config_dir = tmp_path / "config"
        bin_dir.mkdir()
        config_dir.mkdir()

        # Use spawn to avoid fork-related state bleed
        ctx = multiprocessing.get_context("spawn")
        procs = []
        q: Any = ctx.Queue()
        app_ids = [
            "org.test.firefox",
            "org.test.terminal",
            "org.test.editor",
            "org.test.player",
        ]
        for app_id in app_ids:
            p = ctx.Process(
                target=_proc_generate,
                args=(str(bin_dir), str(config_dir), app_id, q),
            )
            p.start()
            procs.append(p)
        for p in procs:
            p.join(timeout=20)

        results = [q.get(timeout=5) for _ in range(len(procs))]
        for tag, value in results:
            assert tag == "ok", f"Subprocess error: {value!r}"
            assert value is True, "Subprocess generation returned False"

        # Every wrapper that should exist does, and is non-empty + executable
        for app_id in app_ids:
            expected_name = app_id.rsplit(".", 1)[-1]
            wrapper = bin_dir / expected_name
            assert wrapper.exists(), f"Missing wrapper for {app_id}"
            assert wrapper.stat().st_size > 0, f"Empty wrapper for {app_id}"
            assert wrapper.stat().st_mode & 0o111, f"Non-executable wrapper {app_id}"
            # No temp file residue
        leftovers = [p for p in bin_dir.iterdir() if p.name.endswith(".tmp")]
        assert not leftovers, f"Temp residue after cross-process generation: {leftovers}"

    def test_cross_process_remove_converges(self, tmp_path: Path) -> None:
        bin_dir = tmp_path / "bin"
        config_dir = tmp_path / "config"
        bin_dir.mkdir()
        config_dir.mkdir()

        # Pre-populate
        gen = WrapperGenerator(bin_dir=str(bin_dir), config_dir=str(config_dir))
        for app_id in ("org.test.firefox", "org.test.terminal"):
            assert gen.generate_wrapper(app_id)

        assert (bin_dir / "firefox").exists()
        assert (bin_dir / "terminal").exists()

        ctx = multiprocessing.get_context("spawn")
        procs = []
        q: Any = ctx.Queue()
        for name in ("firefox", "terminal"):
            p = ctx.Process(
                target=_proc_remove,
                args=(str(bin_dir), str(config_dir), name, q),
            )
            p.start()
            procs.append(p)
        for p in procs:
            p.join(timeout=20)

        results = [q.get(timeout=5) for _ in range(len(procs))]
        for tag, value in results:
            assert tag == "ok", f"Subprocess error: {value!r}"
            assert value is True

        assert not (bin_dir / "firefox").exists()
        assert not (bin_dir / "terminal").exists()
        # No dangling .pref files
        assert not (config_dir / "firefox.pref").exists()
        assert not (config_dir / "terminal.pref").exists()


# ---------------------------------------------------------------------------
# TestStateInvariantsAfterRace: post-condition checks after arbitrary races
# ---------------------------------------------------------------------------


class TestStateInvariantsAfterRace:
    """No matter how the races resolve, the filesystem must obey invariants:
    - every wrapper on disk is non-empty, executable, and parseable
    - every .pref file's name corresponds to an existing wrapper
    - no temp files are left behind
    """

    @pytest.mark.parametrize("seed", [1, 2, 3, 4, 5])
    def test_random_mixed_workload_invariants(
        self, tmp_path: Path, seed: int
    ) -> None:
        import random

        random.seed(seed)
        bin_dir = tmp_path / "bin"
        config_dir = tmp_path / "config"
        bin_dir.mkdir()
        config_dir.mkdir()
        gen = WrapperGenerator(bin_dir=str(bin_dir), config_dir=str(config_dir))
        mgr = WrapperManager(bin_dir=str(bin_dir), config_dir=str(config_dir))

        # Seed a stable set of wrappers first
        seeded = [
            ("org.test.firefox", "firefox"),
            ("org.test.terminal", "terminal"),
            ("org.test.editor", "editor"),
        ]
        for app_id, _name in seeded:
            assert gen.generate_wrapper(app_id)

        actions = [
            ("set_pref", ("firefox", "system")),
            ("set_pref", ("terminal", "auto")),
            ("remove", ("editor",)),
            ("set_pref", ("editor", "flatpak")),  # post-remove
            ("set_pref", ("firefox", "flatpak")),
            ("set_pref", ("terminal", "system")),
            ("remove", ("firefox",)),
            ("set_pref", ("firefox", "auto")),  # post-remove
        ]
        random.shuffle(actions)

        threads = []
        barrier = _barrier(len(actions))

        def run(action: tuple) -> None:
            barrier.wait(timeout=10)
            kind = action[0]
            try:
                if kind == "set_pref":
                    mgr.set_preference(action[1][0], action[1][1])
                elif kind == "remove":
                    mgr.remove_wrapper(action[1][0], force=True)
            except Exception:  # noqa: BLE001 - we're stress-testing
                pass

        for action in actions:
            t = threading.Thread(target=run, args=(action,))
            threads.append(t)
            t.start()
        for t in threads:
            t.join(timeout=30)

        # Invariant 1: every wrapper on disk is non-empty and executable
        for p in bin_dir.iterdir():
            if p.name.endswith(".tmp"):
                continue
            if p.is_file():
                assert p.stat().st_size > 0, f"Empty wrapper on disk: {p}"
                assert p.stat().st_mode & 0o111, f"Non-executable wrapper: {p}"

        # Invariant 2: .pref files reference existing wrappers OR are harmless
        # leftover from a remove-after-set race. The latter is acceptable
        # because remove_wrapper cleans it up; here we just check no .pref
        # references a name with no wrapper *unless* the remove ran AFTER the
        # set — in which case remove should have cleaned it. We treat a
        # dangling .pref as a BUG.
        for p in config_dir.iterdir():
            if p.suffix == ".pref":
                name = p.stem
                wrapper = bin_dir / name
                assert wrapper.exists(), (
                    f"Dangling .pref: {p} (no wrapper {wrapper})"
                )

        # Invariant 3: no temp residue
        assert not list(bin_dir.glob("*.tmp"))
        assert not list(config_dir.glob("*.tmp"))
