# TestConcurrentGenerateRemove: generate vs. remove races
# ---------------------------------------------------------------------------


class TestConcurrentGenerateRemove:
    """``generate_wrapper`` racing with ``remove_wrapper`` on the same name must
    produce a deterministic, recoverable end state — never a half-written
    executable and never a dangling ``.pref``/``.env`` for a missing wrapper.
    """

    def test_repeated_generate_idempotent_under_concurrency(
        self, tmp_path: Path
    ) -> None:
        bin_dir = tmp_path / "bin"
        bin_dir.mkdir()
        config_dir = tmp_path / "config"
        config_dir.mkdir()
        gen = WrapperGenerator(
            bin_dir=str(bin_dir), config_dir=str(config_dir)
        )

        n_threads = 8
        barrier = _barrier(n_threads)
        ok_count = 0
        lock = threading.Lock()

        def worker() -> None:
            nonlocal ok_count
            barrier.wait(timeout=10)
            for _ in range(5):
                if gen.generate_wrapper("org.test.firefox"):
                    with lock:
                        ok_count += 1

        threads = [threading.Thread(target=worker) for _ in range(n_threads)]
        for t in threads:
            t.start()
        for t in threads:
            t.join(timeout=30)

        wrapper = bin_dir / "firefox"
        assert wrapper.exists(), "Wrapper missing after concurrent generation"
        # Idempotence: the same content regardless of which thread "won"
        content = wrapper.read_text()
        assert "Generated by fplaunchwrapper" in content or "#!/bin/sh" in content
        # All threads reported success — generation is intentionally
        # idempotent (it succeeds as long as the wrapper ends up valid).
        assert ok_count == n_threads * 5

    def test_remove_then_concurrent_pref_does_not_resurrect(
        self, tmp_path: Path
    ) -> None:
        """If remove_wrapper succeeds while set_preference races, the .pref
        file must NOT be left behind pointing at a missing binary."""
        mgr, bin_dir, config_dir = _setup_manager(tmp_path)
        _seed_wrapper(bin_dir, config_dir, "firefox")
        mgr.set_preference("firefox", "system")

        # Simulate the race with a tight thread pair
        barrier = _barrier(2)
        results: dict[str, bool] = {}

        def do_remove() -> None:
            barrier.wait(timeout=10)
            # skip the Confirm prompt by injecting force=True
            results["remove"] = mgr.remove_wrapper("firefox", force=True)

        def do_set() -> None:
            barrier.wait(timeout=10)
            results["set"] = mgr.set_preference("firefox", "auto")

        t1 = threading.Thread(target=do_remove)
        t2 = threading.Thread(target=do_set)
        t1.start()
        t2.start()
        t1.join(timeout=15)
        t2.join(timeout=15)

        # Whichever order executed, we must end in a consistent state:
        # either the wrapper exists with a .pref, or it doesn't and there's
        # no .pref pointing at it.
        wrapper_exists = (bin_dir / "firefox").exists()
        pref_exists = (config_dir / "firefox.pref").exists()
        # Dangling .pref for missing wrapper is the bug we want to prevent
        assert not (pref_exists and not wrapper_exists), (
            f"Dangling .pref for removed wrapper. "
            f"results={results!r} wrapper_exists={wrapper_exists} "
            f"pref_exists={pref_exists}"
        )


