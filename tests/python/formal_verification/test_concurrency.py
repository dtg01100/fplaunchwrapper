def _setup_manager(tmp_path: Path) -> tuple[WrapperManager, Path, Path]:
    """Create a WrapperManager pointed at isolated dirs under tmp_path."""
    bin_dir = tmp_path / "bin"
    config_dir = tmp_path / "config"
    bin_dir.mkdir()
    config_dir.mkdir()
    mgr = WrapperManager(bin_dir=str(bin_dir), config_dir=str(config_dir))
    return mgr, bin_dir, config_dir


def _seed_wrapper(bin_dir: Path, config_dir: Path, name: str = "firefox") -> str:
    """Create a minimal valid wrapper file on disk; returns its name."""
    wrapper_path = bin_dir / name
    wrapper_path.write_text(
        "#!/bin/sh\n# Generated by fplaunchwrapper for org.test.app\nexit 0\n"
    )
    wrapper_path.chmod(0o755)
    return name


def _barrier(n: int) -> threading.Barrier:
    return threading.Barrier(n)


# ---------------------------------------------------------------------------
# TestLinearizability: concurrent set_preference on the same wrapper
# ---------------------------------------------------------------------------


class TestLinearizability:
    """Concurrent ``set_preference`` calls on a single wrapper must produce a
    state that corresponds to *some* serial execution of those calls.

    The legal serial orders are any permutation of {system, flatpak, auto}.
    After all threads complete, the persisted ``.pref`` file must contain
    *exactly one* of those three values, never empty, never a fragment.
    """

    PREFERENCES = ("system", "flatpak", "auto")

    def test_concurrent_set_preference_serialize_to_one_value(
        self, tmp_path: Path
    ) -> None:
        mgr, bin_dir, config_dir = _setup_manager(tmp_path)
        _seed_wrapper(bin_dir, config_dir, "firefox")

        n_threads = 16
        n_iterations = 5
        barrier = _barrier(n_threads)
        errors: list[BaseException] = []
        error_lock = threading.Lock()

        def worker(idx: int) -> None:
            try:
                # Spin until all threads are at the barrier so they
                # race as tightly as possible.
                barrier.wait(timeout=10)
                for j in range(n_iterations):
                    pref = self.PREFERENCES[(idx + j) % len(self.PREFERENCES)]
                    ok = mgr.set_preference("firefox", pref)
                    if not ok:
                        raise AssertionError(
                            f"set_preference returned False in worker {idx}"
                        )
            except BaseException as e:  # noqa: BLE001 - propagate to test
                with error_lock:
                    errors.append(e)

        threads = [
            threading.Thread(target=worker, args=(i,)) for i in range(n_threads)
        ]
        for t in threads:
            t.start()
        for t in threads:
            t.join(timeout=30)
        for t in threads:
            assert not t.is_alive(), "Worker thread hung"

        assert not errors, f"Worker errors: {errors!r}"

        pref_file = config_dir / "firefox.pref"
        assert pref_file.exists(), "Preference file missing after concurrent writes"
        content = pref_file.read_text().strip()
        assert content in self.PREFERENCES, (
            f"Concurrent writes left .pref in illegal state: {content!r}"
        )
        # Sanity: the wrapper still exists
        assert (bin_dir / "firefox").exists()

    def test_concurrent_set_preference_does_not_corrupt_partial_write(
        self, tmp_path: Path
    ) -> None:
        """Even under racing writes, ``.pref`` must never be partially written.

        ``atomic_write_text`` guarantees this at the primitive level; this test
        verifies the property end-to-end via the manager API.
        """
        mgr, bin_dir, config_dir = _setup_manager(tmp_path)
        _seed_wrapper(bin_dir, config_dir, "firefox")

        n_threads = 12
        barrier = _barrier(n_threads)

        def worker(idx: int) -> None:
            barrier.wait(timeout=10)
            for _ in range(20):
                pref = self.PREFERENCES[idx % len(self.PREFERENCES)]
                assert mgr.set_preference("firefox", pref)

        threads = [
            threading.Thread(target=worker, args=(i,)) for i in range(n_threads)
        ]
        for t in threads:
            t.start()
        for t in threads:
            t.join(timeout=30)

        # Read .pref *while another writer is active* — but here writers are
        # done; instead, verify the temp-file residue is gone.
        pref_file = config_dir / "firefox.pref"
        assert pref_file.exists()
        # No leftover .tmp siblings in the config dir
        leftovers = [p for p in config_dir.iterdir() if p.name.endswith(".tmp")]
        assert not leftovers, f"Temp file residue after concurrent writes: {leftovers}"


