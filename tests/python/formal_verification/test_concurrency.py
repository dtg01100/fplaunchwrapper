"""Phase 10: Concurrency / linearizability verification.

Real multi-threaded and multi-process tests against the wrapper
manager and generator. Verifies that:

1. Concurrent ``set_preference`` calls on the same wrapper serialize
   into one of the legal total orderings (linearizability).
2. Concurrent ``generate_wrapper`` / ``remove_wrapper`` on the same
   wrapper never produces a half-written executable on disk.
3. Two processes using separate ``WrapperManager`` instances over
   the same ``bin_dir`` / ``config_dir`` converge to a consistent
   state.
4. ``remove_wrapper`` interleaved with ``set_preference`` cannot
   resurrect a wrapper or leave a dangling ``.pref`` / ``.env``
   pointing at a missing binary.

The existing test suite relies on serial execution; these tests
use ``threading`` and ``multiprocessing`` to exercise the actual
concurrency surface called out as a limitation in
``docs/FORMAL_VERIFICATION_REPORT.md``.
"""
from __future__ import annotations

import multiprocessing
