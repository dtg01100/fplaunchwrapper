# Coverage gap: lines 341-342 - _cleanup_stale_lock invalid timestamp
# ---------------------------------------------------------------------------
class TestCleanupStaleLockInvalidTimestamp:
    """Lines 341-342 catch ``ValueError`` from ``int(parts[1])``.

    This branch is reached when:
    - ``pidfile`` exists
    - the PID is alive (so ``os.kill(pid, 0)`` succeeds)
    - the timestamp portion is not a valid integer
    The function then falls through to ``return False`` because the
    PID is alive and we can't determine staleness from the timestamp.
    """

    def test_alive_pid_with_garbage_timestamp_returns_false(
        self, tmp_path: Path,
    ) -> None:
        lockfile = tmp_path / "test.lock"
        pidfile = tmp_path / "test.pid"
        lockfile.mkdir()
        # Our own PID (alive) with a non-integer timestamp
        pidfile.write_text(f"{os.getpid()}:not-an-int")

        result = _cleanup_stale_lock(lockfile, pidfile)
        assert result is False
        # Lock is still held because the PID is alive
        assert lockfile.exists()
        assert pidfile.exists()


