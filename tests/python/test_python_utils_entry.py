# Coverage gap: lines 356-357 - _cleanup_stale_lock outer exception handler
# ---------------------------------------------------------------------------
class TestCleanupStaleLockOuterException:
    """Lines 356-357 catch ``(OSError, ValueError, IOError)`` raised
    from anywhere in the function body that isn't otherwise suppressed.

    We trigger it by raising an OSError from the very first operation
    (``pidfile.exists()``) which is the only path that can escape
    with a non-suppressed exception in normal code flow.
    """

    def test_outer_oserror_returns_false(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        lockfile = tmp_path / "test.lock"
        pidfile = tmp_path / "test.pid"
        lockfile.mkdir()

        real_exists = Path.exists

        def fake_exists(self: Path) -> bool:
            if str(self) == str(pidfile):
                raise OSError("simulated exists() failure")
            return real_exists(self)

        monkeypatch.setattr(Path, "exists", fake_exists)
        result = _cleanup_stale_lock(lockfile, pidfile)
        assert result is False


