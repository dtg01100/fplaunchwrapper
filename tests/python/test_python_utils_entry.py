# Coverage gap: line 75 - validate_home_dir symlink-after-resolve branch
# ---------------------------------------------------------------------------
class TestValidateHomeDirSymlinkBranch:
    """Line 75 re-resolves ``abs_dir`` when the resolved path is still a symlink.

    In practice ``Path.resolve()`` follows symlinks, so the ``is_symlink``
    check on line 74 is normally False. We force the branch to be
    exercised by monkeypatching ``Path.is_symlink`` to return True for
    the resolved path.
    """

    def test_resolve_again_when_still_symlink(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        monkeypatch.setenv("HOME", str(tmp_path))
        target = tmp_path / "real"
        target.mkdir()
        link = tmp_path / "link"
        link.symlink_to(target)

        real_is_symlink = Path.is_symlink

        def fake_is_symlink(self: Path) -> bool:
            if str(self) == str(target.resolve()):
                return True
            return real_is_symlink(self)

        monkeypatch.setattr(Path, "is_symlink", fake_is_symlink)
        result = validate_home_dir(link)
        assert result is not None
        assert str(tmp_path) in result


