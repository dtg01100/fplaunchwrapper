# Coverage gap: lines 121-122 - is_wrapper_file exception handler
# ---------------------------------------------------------------------------
class TestIsWrapperFileExceptionHandler:
    """Lines 121-122 are reached when the try block raises an
    ``(IOError, OSError, UnicodeDecodeError, re.error)``. We trigger
    the OSError branch by patching ``Path.open`` to raise.
    """

    def test_open_oserror_returns_false(self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        target = _write_wrapper(tmp_path / "wrap.sh")

        def fake_open(self: Path, *args: object, **kwargs: object):  # type: ignore[no-untyped-def]
            raise OSError("simulated open failure")

        monkeypatch.setattr(Path, "open", fake_open)
        assert is_wrapper_file(target) is False

    def test_stat_oserror_returns_false(self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        """``path.stat()`` raising OSError is also caught by the handler."""
        target = _write_wrapper(tmp_path / "wrap.sh")
        monkeypatch.setattr(
            Path, "stat",
            mock.Mock(side_effect=OSError("simulated stat failure")),
        )
        assert is_wrapper_file(target) is False


